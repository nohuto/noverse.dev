(function attachPseudocodeDiffSource(global) {
  'use strict';

  const SETTINGS_KEY = 'nv-diff-pseudocode-settings-v1';
  const DEFAULT_LEFT_RELEASE = '11-23H2';
  const DEFAULT_RIGHT_RELEASE = '11-24H2';
  const DEFAULT_MODULE = 'ntoskrnl';
  const settingsStore = global.createNVDiffSettingsStore({
    key: SETTINGS_KEY,
    defaults: () => ({
      normalization: normalizationDefaults(),
      showFullFunction: true
    }),
    normalize: (candidate, defaults) => {
      const normalization = { ...defaults.normalization };
      Object.keys(normalization).forEach(key => {
        if (candidate?.normalization?.[key] !== undefined) {
          normalization[key] = Boolean(candidate.normalization[key]);
        }
      });
      return {
        normalization,
        showFullFunction: candidate?.showFullFunction !== undefined
          ? Boolean(candidate.showFullFunction)
          : defaults.showFullFunction
      };
    }
  });
  const source = global.createNVDiffManifestSource({
    repository: 'nohuto/decompiled-pseudocode',
    dataset: 'decompiled-pseudocode',
    cacheKey: 'nv-diff-pseudocode-name-cache-v1'
  });

  const normalizationDefaults = () => ({
    stripCrossReferenceMetadata: true,
    normalizeRelocationSymbols: true,
    stripStorageLocationComments: true,
    normalizeDecompilerIdentifiers: true,
    normalizeNumericNotation: true,
    normalizeGeneratedLabels: false,
    normalizePrototypeExpansionArgs: false,
    trimTrailingWhitespace: true,
    ...(global.Normalization?.DEFAULTS || {})
  });

  const readSettings = settingsStore.read;
  const writeSettings = settingsStore.write;
  const resetSettings = settingsStore.reset;

  const renderSettings = (body, onChange) => {
    const settings = readSettings();
    body.replaceChildren();
    const inputs = {
      stripCrossReferenceMetadata: settingsStore.addCheckbox(body, 'diff-setting-strip-cross-reference-metadata', 'Hide XREF comments', settings.normalization.stripCrossReferenceMetadata),
      normalizeRelocationSymbols: settingsStore.addCheckbox(body, 'diff-setting-normalize-relocation-symbols', 'Hide build specific addresses/symbols', settings.normalization.normalizeRelocationSymbols),
      stripStorageLocationComments: settingsStore.addCheckbox(body, 'diff-setting-strip-storage-location-comments', 'Hide register/stack comments', settings.normalization.stripStorageLocationComments),
      normalizeDecompilerIdentifiers: settingsStore.addCheckbox(body, 'diff-setting-normalize-decompiler-identifiers', 'Normalize autogenerated variable/argument names', settings.normalization.normalizeDecompilerIdentifiers),
      normalizeNumericNotation: settingsStore.addCheckbox(body, 'diff-setting-normalize-numeric', 'Treat equivalent numbers as same (hex/dec/suffix)', settings.normalization.normalizeNumericNotation),
      normalizeGeneratedLabels: settingsStore.addCheckbox(body, 'diff-setting-normalize-labels', 'Ignore generated label number changes', settings.normalization.normalizeGeneratedLabels),
      normalizePrototypeExpansionArgs: settingsStore.addCheckbox(body, 'diff-setting-normalize-prototype', 'Ignore extra trailing default arguments', settings.normalization.normalizePrototypeExpansionArgs),
      showFullFunction: settingsStore.addCheckbox(body, 'diff-setting-show-full', 'Show full function', settings.showFullFunction),
      trimTrailingWhitespace: settingsStore.addCheckbox(body, 'diff-setting-trim-whitespace', 'Ignore whitespace only changes', settings.normalization.trimTrailingWhitespace)
    };

    const sync = () => {
      writeSettings({
        normalization: {
          stripCrossReferenceMetadata: inputs.stripCrossReferenceMetadata.checked,
          normalizeRelocationSymbols: inputs.normalizeRelocationSymbols.checked,
          stripStorageLocationComments: inputs.stripStorageLocationComments.checked,
          normalizeDecompilerIdentifiers: inputs.normalizeDecompilerIdentifiers.checked,
          normalizeNumericNotation: inputs.normalizeNumericNotation.checked,
          normalizeGeneratedLabels: inputs.normalizeGeneratedLabels.checked,
          normalizePrototypeExpansionArgs: inputs.normalizePrototypeExpansionArgs.checked,
          trimTrailingWhitespace: inputs.trimTrailingWhitespace.checked
        },
        showFullFunction: inputs.showFullFunction.checked
      });
      onChange();
    };
    Object.values(inputs).forEach(input => input.addEventListener('change', sync));
  };

  const normalizeText = source => String(source || '').replace(/\r\n?/g, '\n');

  const preparePair = (leftSource, rightSource) => {
    const settings = readSettings();
    const result = global.Normalization?.preparePair
      ? global.Normalization.preparePair(leftSource, rightSource, settings.normalization)
      : { leftText: normalizeText(leftSource), rightText: normalizeText(rightSource), equivalent: false };
    const fullContext = Math.max(result.leftText.split('\n').length, result.rightText.split('\n').length) + 2;
    return {
      leftText: result.leftText,
      rightText: result.rightText,
      equivalent: Boolean(result.equivalent),
      context: settings.showFullFunction ? fullContext : 4
    };
  };

  const prepareSingle = source => {
    const settings = readSettings();
    return global.Normalization?.normalize
      ? global.Normalization.normalize(source, settings.normalization).text
      : normalizeText(source);
  };

  global.NVDiffSources = global.NVDiffSources || {};
  global.NVDiffSources.pseudocode = {
    ...source,
    defaultLeft: DEFAULT_LEFT_RELEASE,
    defaultRight: DEFAULT_RIGHT_RELEASE,
    defaultModule: DEFAULT_MODULE,
    extraScripts: ['/main/min/normalization.min.js'],
    preparePair,
    prepareSingle,
    renderSettings,
    resetSettings,
    highlightBlockComments: true
  };
})(window);
